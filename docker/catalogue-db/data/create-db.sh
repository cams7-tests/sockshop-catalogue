#!/bin/bash
psql -v ON_ERROR_STOP=1 --username "$POSTGRES_USER" <<-EOSQL
  CREATE USER $CATALOGUE_DB_USER WITH PASSWORD '$CATALOGUE_DB_PASS';
  CREATE DATABASE $CATALOGUE_DB_NAME;
  GRANT ALL PRIVILEGES ON DATABASE $CATALOGUE_DB_NAME TO $CATALOGUE_DB_USER;
EOSQL
psql -v ON_ERROR_STOP=1 --username "$CATALOGUE_DB_USER" --dbname "$CATALOGUE_DB_NAME" -f /tmp/dump.sql
